nouns = ["стол", "стул", "дом", "машина", "кот", "собака", "эскалатор"]

word = input("Введите русское слово: ").lower()

if word in nouns:
  print(f"Слово '{word}' - скорее всего, существительное (в нашем ограниченном словаре).")
else:
  print(f"Слово '{word}' - не найдено в нашем словаре существительных.")

